function fruitCounter(array){
  let objeto = {};
  array.sort();
  let teste = array[0];
  let cont = 0;
  for(let i = 0;i<array.length;i+=1){
      if(array[i] === teste){
          cont +=1;
      } else if (array[i] != teste){
          objeto[teste] = [cont];
          cont = 0;
          teste = array[i];
      }

      
    }   
  



    return objeto;
}
